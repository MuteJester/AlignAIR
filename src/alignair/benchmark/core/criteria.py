"""Benchmark criteria catalog.

This is the human-readable assay plan encoded as data. The evaluator emits these
criteria alongside numeric results so every benchmark report explains what was
tested and under which contexts it should be interpreted.
"""
from __future__ import annotations

from dataclasses import asdict, dataclass


@dataclass(frozen=True)
class BenchmarkCriterion:
    category: str
    name: str
    metric_keys: tuple[str, ...]
    description: str
    contexts: tuple[str, ...]
    importance: str = "core"
    status: str = "available"
    required_outputs: tuple[str, ...] = ()
    ground_truth_fields: tuple[str, ...] = ()
    failure_modes: tuple[str, ...] = ()
    interpretation: str = ""

    def to_dict(self) -> dict:
        return asdict(self)


@dataclass(frozen=True)
class ScenarioAxis:
    name: str
    values: tuple[str, ...]
    description: str
    why_it_matters: str

    def to_dict(self) -> dict:
        return asdict(self)


